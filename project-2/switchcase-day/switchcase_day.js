let day = Number (prompt("enter any day"));

switch(day){
    case 1:
        alert("sunday");
        break;
    case 2:
        alert("monday");
        break;
    case 3:
        alert("tuesday");
        break;
    case 4:
        alert("wednesday");
        break;
    case 5:
        alert("thursday");
        break;
    case 6:
        alert("friday");
        break;
    case 7:
        alert("saturday");
        break;
    default :
        alert("invaild number");
        break;
}
